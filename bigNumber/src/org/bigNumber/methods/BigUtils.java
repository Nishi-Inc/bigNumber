package org.bigNumber.methods;

import org.bigNumber.BigNumber;

public abstract class BigUtils extends BigMain {
	
	private static final long serialVersionUID = 1L;
	
	/**
	 * Private constructor to prevent creation of objects
	 */
	private BigUtils() {}

	public static BigNumber add() {
		BigNumber result = new BigNumber();
		return result;
	}
	
}
